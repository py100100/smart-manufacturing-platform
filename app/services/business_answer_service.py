# ── 业务答案调度 ──────────────────────────────────────────


def _generate_business_answer(
    request_text: str,
    business_domains: dict[str, bool],
    knowledge_domains: dict[str, bool],
) -> str | None:
    """根据业务领域命中情况，分派到对应的本地模板。

    调度优先级：
    1. 知识图谱 / 机器人控制 / 预测性维护专项模板（已有）
    2. 五大业务模块模板（新增）
    3. 通用知识模板（兜底）

    返回 None 表示无匹配模板。
    """
    # 知识领域专项模板（保持已有逻辑不变）
    if knowledge_domains.get("knowledge_graph", False):
        return _generate_knowledge_graph_answer()
    if knowledge_domains.get("robotics_control", False):
        return _generate_robotics_control_answer()
    if _should_use_predictive_maintenance_template(knowledge_domains):
        return _generate_predictive_maintenance_answer(knowledge_domains)

    # 五大业务模块模板（按命中数优先级排序，取命中最多者）
    biz_hits = sorted(
        [(domain, hit) for domain, hit in business_domains.items() if hit],
        key=lambda x: sum(
            1 for kw in BUSINESS_DOMAIN_KEYWORDS.get(x[0], [])
            if kw in request_text.lower()
        ),
        reverse=True,
    )
    if biz_hits:
        domain_name = biz_hits[0][0]
        if domain_name == "production_scheduling":
            return _generate_production_scheduling_answer()
        elif domain_name == "quality_inspection":
            return _generate_quality_inspection_answer()
        elif domain_name == "predictive_maintenance":
            return _generate_predictive_maintenance_business_answer()
        elif domain_name == "supply_chain_management":
            return _generate_supply_chain_management_answer()
        elif domain_name == "process_parameter_optimization":
            return _generate_process_parameter_optimization_answer()

    # 通用知识模板兜底
    if any(knowledge_domains.values()):
        return _generate_local_knowledge_answer(request_text)

    # 最后兜底：如果五大业务领域任一中了一项，但以上全未匹配
    # （例如质量问题只命中 1 个关键词），则按命中数最高者走对应模板
    if any(business_domains.values()):
        best = max(
            (d for d, hit in business_domains.items() if hit),
            key=lambda d: sum(
                1 for kw in BUSINESS_DOMAIN_KEYWORDS.get(d, [])
                if kw in request_text.lower()
            ),
            default=None,
        )
        if best == "production_scheduling":
            return _generate_production_scheduling_answer()
        elif best == "quality_inspection":
            return _generate_quality_inspection_answer()
        elif best == "predictive_maintenance":
            return _generate_predictive_maintenance_business_answer()
        elif best == "supply_chain_management":
            return _generate_supply_chain_management_answer()
        elif best == "process_parameter_optimization":
            return _generate_process_parameter_optimization_answer()

    return None


