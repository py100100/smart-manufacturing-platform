def _detect_business_domains(text: str) -> dict[str, bool]:
    """检测请求文本覆盖的五大业务领域。

    返回 {domain_name: bool}，domain_name 为智能体英文名。
    与 _detect_knowledge_domains 互补：前者覆盖专项技术领域，
    本函数覆盖五大业务模块。
    """
    text_lower = text.lower()
    return {
        domain: any(kw in text_lower for kw in keywords)
        for domain, keywords in BUSINESS_DOMAIN_KEYWORDS.items()
    }


def _is_business_explanation_question(text: str) -> bool:
    """判断是否为业务解释型问题。

    触发条件（比 _matches_knowledge_question 更宽松）：
    1. 命中至少一个解释型问法（如何/怎么/为什么/请分析/优化方案…）
    2. 命中至少一个业务领域关键词（五大模块）或至少一个知识领域关键词
    3. 文本长度 ≥ 4 字符

    这样普通业务问题如"如何优化排产？"也能触发增强层，
    而不是只依赖 ≥2 个知识模式。
    """
    if len(text) < 4:
        return False

    text_lower = text.lower()

    # 条件 A：命中至少一个解释型问法
    has_explanation = any(
        _pat_search(pat, text_lower) for pat in EXPLANATION_PATTERNS
    )
    if not has_explanation:
        return False

    # 条件 B：命中至少一个业务领域或知识领域
    biz_domains = _detect_business_domains(text)
    has_biz_domain = any(biz_domains.values())

    knowledge_domains = _detect_knowledge_domains(text)
    has_knowledge_domain = any(knowledge_domains.values())

    return has_biz_domain or has_knowledge_domain


def _detect_all_domains(text: str) -> dict[str, bool]:
    """合并业务领域 + 知识领域检测结果。"""
    biz = _detect_business_domains(text)
    knowledge = _detect_knowledge_domains(text)
    return {**biz, **knowledge}

