# 如果问题短于这个长度且命中了知识模式，走知识解答
MIN_KNOWLEDGE_QUESTION_LENGTH: int = 8


def _pat_search(pat: str, text: str) -> bool:
    """跨字搜索：先直匹配，再移除中文之间空白后匹配。"""
    if pat in text:
        return True
    # "结合 XXX 与 YYY" → "结合XXX与YYY"
    compact_text = re.sub(r"(?<=[一-龥])\s+(?=[一-龥])", "", text)
    compact_pat = re.sub(r"(?<=[一-龥])\s+(?=[一-龥])", "", pat)
    if compact_pat in compact_text:
        return True
    # 正则匹配（处理 "结合.*与" 这类模式）
    try:
        if re.search(pat, text):
            return True
        if re.search(pat, compact_text):
            return True
    except re.error:
        pass
    return False


def _matches_knowledge_question(text: str) -> bool:
    """检查用户输入是否为知识解释型问题。

    只做关键词匹配，不调模型，保证路由阶段零延迟。
    保守策略：文本须 ≥ MIN_KNOWLEDGE_QUESTION_LENGTH 且命中 ≥2 个模式才判定。
    """
    if len(text) < MIN_KNOWLEDGE_QUESTION_LENGTH:
        return False
    text_lower = text.lower()
    hits = sum(
        1 for pat in KNOWLEDGE_QUESTION_PATTERNS
        if _pat_search(pat, text_lower)
    )
    # 宽松阈值：命中 ≥2 个模式即判定为知识型
    return hits >= 2


