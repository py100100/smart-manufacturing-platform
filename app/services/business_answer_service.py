# ── 空洞回答禁止模式 ──────────────────────────────────────

# 命中任一模式即判定为空洞回答，不可作为最终 summary
HOLLOW_PATTERNS: list[str] = [
    "智能体已接收请求",
    "请提供完整数据",
    "建议通过结构化接口传入数据",
    "系统调度了",  # 通用前缀，覆盖"系统调度了若干/3个/N个智能体..."
    "个智能体协同分析",  # 覆盖"N个智能体协同分析..."
    "请查看各步骤输出",
    "综合分析完成",
    "建议按优先级处理",
    "协同分析完成",
    "提供更多信息",
    "请输入",
    "缺少必要参数",
    "请补充",
    "无法处理",
    "暂不支持",
]

# 最终 summary 最低质量阈值
MIN_SUMMARY_CHINESE_CHARS: int = 300
MIN_SUMMARY_SUBHEADINGS: int = 3
MIN_SUMMARY_SPECIFIC_STEPS: int = 5


def _count_chinese_chars(text: str) -> int:
    """统计中文字符数量（含中文标点）。"""
    count = 0
    for ch in text:
        cp = ord(ch)
        if (0x4E00 <= cp <= 0x9FFF
                or 0x3400 <= cp <= 0x4DBF
                or 0x3000 <= cp <= 0x303F
                or 0xFF00 <= cp <= 0xFFEF
                or 0x2000 <= cp <= 0x206F):
            count += 1
    return count


def _count_subheadings(text: str) -> int:
    """统计小标题/分段数量：以【开头的行、## 开头的行、编号段落。"""
    import re
    count = 0
    # 【...】 中文方括号标题
    count += len(re.findall(r'【[^】]+】', text))
    # 纯数字编号段落（如 "1. xxx"、"1) xxx"）
    numbered = len(re.findall(r'^\d+[\.\)、]\s*\S', text, re.MULTILINE))
    count += numbered
    # Markdown 标题
    count += len(re.findall(r'^#{1,3}\s', text, re.MULTILINE))
    return count


def _count_specific_steps(text: str) -> int:
    """统计具体建议/步骤条数：以 - 或 • 或编号开头的行、含具体指标的行。"""
    import re
    count = 0
    # - xxx 或 • xxx 条目
    count += len(re.findall(r'^\s*[-•]\s+\S', text, re.MULTILINE))
    # 编号步骤 (a) (b) 等
    count += len(re.findall(r'^\s*\([a-z]\)\s', text, re.MULTILINE))
    # 编号步骤 1. 2. 等
    count += len(re.findall(r'^\s*\d+[\.\)、]\s*\S', text, re.MULTILINE))
    return count


def _has_business_keywords(text: str) -> bool:
    """检查文本是否至少包含一定数量的业务关键词。"""
    business_keywords = [
        "排产", "调度", "工单", "产能", "交期",
        "质量", "缺陷", "不良", "根因", "检测",
        "设备", "故障", "维护", "振动", "传感器", "预测",
        "库存", "采购", "供应链", "物料", "BOM", "供应商",
        "工艺", "参数", "温度", "压力", "优化",
        "方案", "建议", "步骤", "方法", "分析", "评估",
        "公式", "指标", "措施", "流程", "策略",
    ]
    text_lower = text.lower()
    hits = sum(1 for kw in business_keywords if kw.lower() in text_lower)
    return hits >= 3


def validate_summary(text: str | None) -> tuple[bool, list[str]]:
    """验证 summary 是否为空洞回答。

    Args:
        text: 待验证的 summary 文本。

    Returns:
        (is_valid, violations): is_valid=True 表示通过验证；
        violations 列出所有违规原因（空列表表示通过）。
    """
    if not text or not text.strip():
        return False, ["summary 为空"]

    violations: list[str] = []

    # 1) 禁止模式检查
    for pattern in HOLLOW_PATTERNS:
        if pattern in text:
            violations.append(f"包含禁止短语: {pattern}")

    # 2) 中文字符数
    chinese_chars = _count_chinese_chars(text)
    if chinese_chars < MIN_SUMMARY_CHINESE_CHARS:
        violations.append(
            f"中文字符数不足: {chinese_chars} < {MIN_SUMMARY_CHINESE_CHARS}"
        )

    # 3) 小标题/分段数
    subheadings = _count_subheadings(text)
    if subheadings < MIN_SUMMARY_SUBHEADINGS:
        violations.append(
            f"小标题/分段数不足: {subheadings} < {MIN_SUMMARY_SUBHEADINGS}"
        )

    # 4) 具体建议/步骤条数
    steps = _count_specific_steps(text)
    if steps < MIN_SUMMARY_SPECIFIC_STEPS:
        violations.append(
            f"具体建议/步骤数不足: {steps} < {MIN_SUMMARY_SPECIFIC_STEPS}"
        )

    # 5) 业务关键词
    if not _has_business_keywords(text):
        violations.append("缺少业务关键词")

    # 6) 不能只列字段名（连续出现 >5 个 "xxx：" 模式且无解释文字）
    field_name_pattern = len(re.findall(r'\w{2,8}[：:]', text))
    if field_name_pattern > 10 and chinese_chars < 500:
        violations.append("疑似只列字段名，缺少实质性解释")

    # 7) 不能只要求用户补数据
    data_request_patterns = [
        "请提供.*数据", "请传入.*参数", "请填写", "请输入.*信息",
    ]
    for pat in data_request_patterns:
        if re.search(pat, text):
            violations.append(f"疑似只要求用户补数据: 命中 {pat}")
            break

    return (len(violations) == 0, violations)


class BusinessAnswerService:
    """业务问答增强服务。

    职责：
    - 判断用户问题是否属于"业务解释型问题"
    - 检测问题覆盖的业务领域（五大模块 + 知识领域）
    - 生成高质量业务答案（LLM 优先，本地专家模板兜底）
    - 返回可直接放入 summary 展示的内容

    用法:
        svc = BusinessAnswerService(llm_client=deepseek_client, registry=agent_registry)
        if svc.is_business_explanation_question(text):
            answer = await svc.generate_answer(text, agent_names=["supply_chain_management"])
    """

    def __init__(self, llm_client: Any = None, registry: Any = None) -> None:
        """初始化服务。

        Args:
            llm_client: DeepSeekClient 实例（可选）。未提供时仅使用本地模板。
            registry: AgentRegistry 实例（可选）。用于获取智能体中文显示名。
        """
        self._llm = llm_client
        self._registry = registry

    # ── 公开 API ──────────────────────────────────────────────

    @staticmethod
    def is_knowledge_question(text: str) -> bool:
        """判断是否为知识解释型问题（保留，向后兼容）。"""
        return _matches_knowledge_question(text)

    @staticmethod
    def is_business_explanation_question(text: str) -> bool:
        """判断是否为业务解释型问题。

        触发条件比 is_knowledge_question 更宽松：
        命中至少一个解释型问法 + 至少一个业务领域/知识领域关键词即可。
        覆盖五大业务模块的普通问题。
        """
        return _is_business_explanation_question(text)

    @staticmethod
    def detect_domains(text: str) -> dict[str, bool]:
        """检测问题覆盖的知识领域（保留，向后兼容）。"""
        return _detect_knowledge_domains(text)

    @staticmethod
    def detect_business_domains(text: str) -> dict[str, bool]:
        """检测问题覆盖的五大业务领域。"""
        return _detect_business_domains(text)

    @staticmethod
    def detect_all_domains(text: str) -> dict[str, bool]:
        """检测问题覆盖的所有领域（业务 + 知识）。"""
        return _detect_all_domains(text)

    async def generate_answer(
        self, request_text: str, agent_names: list[str],
        use_llm: bool = True,
    ) -> str | None:
        """生成业务解释型答案。

        当 use_llm=True 时优先调 LLM，失败后回退到本地模板。
        当 use_llm=False 时直接走本地模板，不访问 DeepSeekClient。
        本地模板按优先级：业务模块模板 → 知识专项模板 → 通用知识模板。
        只在本地模板也无法生成时才返回 None。

        所有答案均经过 validate_summary 过滤：空洞回答会被丢弃，
        回退到下一级来源（LLM 空洞→本地模板，本地模板空洞→通用兜底）。
        """
        # 1) 尝试 LLM（仅在 use_llm=True 时）
        if use_llm:
            llm_answer = await self._try_llm_answer(request_text, agent_names)
            if llm_answer:
                is_valid, violations = validate_summary(llm_answer)
                if is_valid:
                    return llm_answer
                logger.warning(
                    "LLM 返回的答案未通过质量验证，回退本地模板: %s",
                    "; ".join(violations),
                )
            logger.info("LLM 不可用或答案空洞，使用本地规则生成业务答案")

        # 2) 本地规则模板
        biz_domains = _detect_business_domains(request_text)
        knowledge_domains = _detect_knowledge_domains(request_text)
        local_answer = _generate_business_answer(
            request_text, biz_domains, knowledge_domains
        )

        # 3) 验证本地模板答案
        if local_answer is not None:
            is_valid, violations = validate_summary(local_answer)
            if is_valid:
                return local_answer
            logger.warning(
                "本地模板答案未通过质量验证: %s", "; ".join(violations)
            )
            # 本地模板不合格时，尝试通用知识模板（如果还未使用过）
            if not any(knowledge_domains.values()):
                # 强制走通用知识模板作为最后兜底
                fallback = _generate_local_knowledge_answer(request_text)
                if fallback is not None:
                    is_valid_fb, _ = validate_summary(fallback)
                    if is_valid_fb:
                        return fallback
            # 本地答案不合格且无可用兜底 → 返回 None，
            # 避免服务层直接返回不合格文本污染 Orchestrator summary
            logger.warning(
                "本地业务答案未通过质量验证且无可用兜底，返回 None"
            )
            return None

        return None

    # ── LLM 答案生成 ──────────────────────────────────────────

    async def _try_llm_answer(
        self, request_text: str, agent_names: list[str]
    ) -> str | None:
        """尝试用 LLM 生成答案；不可用时返回 None。"""
        if self._llm is None or not self._llm.is_configured:
            return None

        agent_display_names = self._resolve_display_names(agent_names)
        system_prompt = (
            "你是一个智能制造领域的高级技术专家。"
            "用户正在询问一个专业技术问题，请直接、专业、结构化地回答。"
            "答案必须包含以下部分："
            "1) 问题直接回答"
            "2) 方法步骤（分1、2、3…）"
            "3) 关键公式或核心逻辑"
            "4) 实施建议"
            "5) 注意事项或风险控制"
            "请使用中文，避免空泛套话，提供可执行的具体建议。"
        )
        user_prompt = (
            f"用户问题：{request_text}\n\n"
            f"系统已将问题路由到以下智能体进行分析："
            f"{', '.join(agent_display_names)}。"
            f"请根据你的专业知识直接回答用户问题，"
            f"给出完整的技术方案。"
        )

        try:
            result = await self._llm.generate_with_system_prompt(
                system_prompt, user_prompt
            )
            content = (result.get("content") or "").strip()
            if content:
                return content
            return None
        except Exception as exc:
            logger.warning("LLM 答案生成失败: %s", exc)
            return None

    def _resolve_display_names(self, agent_names: list[str]) -> list[str]:
        """将智能体英文名解析为中文显示名。"""
        if self._registry is None:
            # 无注册中心时使用内置映射
            name_map: dict[str, str] = {
                "production_scheduling": "生产调度优化",
                "quality_inspection": "质量检测与缺陷分析",
                "predictive_maintenance": "设备预测性维护",
                "supply_chain_management": "供应链协同管理",
                "process_parameter_optimization": "工艺参数优化",
            }
            return [name_map.get(n, n) for n in agent_names]

        result: list[str] = []
        for name in agent_names:
            try:
                result.append(self._registry.meta(name).display_name)
            except Exception:
                result.append(name)
        return result
