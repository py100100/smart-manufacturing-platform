def _should_use_predictive_maintenance_template(
    domains: dict[str, bool]
) -> bool:
    """判断是否应使用预测性维护专用模板。

    条件：传感器 + 阈值 + 误报漏报 三个核心领域同时命中，
    且 SPC 或 深度学习 任一命中。
    """
    return (
        domains.get("sensor", False)
        and domains.get("threshold", False)
        and domains.get("fpfn", False)
        and (domains.get("spc", False) or domains.get("deep_learning", False))
    )


def _generate_predictive_maintenance_answer(domains: dict[str, bool]) -> str:
    """预测性维护专用回答模板。

    当命中传感器 + 阈值 + 误报漏报 + (SPC 或 深度学习) 时，
    生成 6 步结构化方案：数据基线 → SPC 控制限 → 深度学习预测 →
    阈值自适应修正 → 报警决策 → 落地建议。
    """
    has_spc = domains.get("spc", False)
    has_dl = domains.get("deep_learning", False)
    has_rul = domains.get("rul", False)
    has_threshold = domains.get("threshold", False)
    has_fpfn = domains.get("fpfn", False)
    has_sensor = domains.get("sensor", False)

    lines: list[str] = []

    # ── 标题与概述 ──
    lines.append("针对您的问题，以下是一个系统性的技术方案：\n")

    lines.append("【方案概述】")
    if has_spc and has_dl:
        lines.append(
            "建议采用「统计过程控制（SPC）+ 深度学习预测残差」"
            "的自适应报警阈值方法。核心思路：先用历史健康数据建立传感器基准分布，"
            "通过 SPC 控制图捕获短期异常；再用深度学习模型预测未来趋势，"
            "将预测残差作为阈值修正项；最终报警由控制限、模型不确定性和设备健康状态"
            "三者共同决定，从而显著降低固定阈值造成的误报与漏报。"
        )
    elif has_spc:
        lines.append(
            "建议采用统计过程控制（SPC）结合设备健康评估的自适应报警方法。"
            "核心思路：基于健康工况历史数据建立 SPC 动态控制限，"
            "根据设备退化趋势自适应调整报警边界。"
        )
    elif has_dl:
        lines.append(
            "建议采用深度学习预测残差驱动的自适应报警方法。"
            "核心思路：使用 LSTM/Transformer 学习传感器正常行为模式，"
            "以预测残差作为异常强度指标，动态调整报警阈值。"
        )
    else:
        lines.append(
            "建议采用数据驱动的自适应阈值方法，通过统计分析与趋势监测"
            "动态调整报警边界，平衡检出率与误报率。"
        )
    lines.append("")

    # ── 六步方案 ──
    lines.append("【方法步骤】")

    step = 1

    # 步骤 1：数据基线建模
    lines.append(f"{step}. 数据基线建模")
    step += 1
    lines.append(
        "   使用设备健康状态下的振动、温度、电流等多传感器历史数据，"
        "建立基线统计特征："
    )
    lines.append("   - 各传感器通道的均值 μ_i、标准差 σ_i、分位数 P_95")
    lines.append("   - 健康状态下的 EWMA 平滑序列作为动态基线")
    lines.append("   - 工况分割：区分启动、稳定运行、负载变化等不同阶段，"
                 "为每个阶段建立独立基线")
    if has_rul:
        lines.append(
            "   - 以历史完整退化到故障的数据标定剩余使用寿命（RUL）标签，"
            "作为模型训练目标"
        )

    # 步骤 2：SPC 动态控制限
    lines.append(f"{step}. SPC 动态控制限")
    step += 1
    lines.append(
        "   在健康基线基础上建立统计过程控制（SPC）动态控制限："
    )
    lines.append("   - 对短期波动使用 EWMA（指数加权移动平均）平滑：")
    lines.append("       Z_t = λ·X_t + (1-λ)·Z_{t-1}，λ 取 0.1~0.3")
    lines.append("   - 对小幅持续偏移使用 CUSUM（累积和）检测：")
    lines.append("       S_t = max(0, S_{t-1} + (X_t - μ_0) - k)")
    lines.append("       其中 k 为容许偏移量（通常取 0.5σ），"
                 "当 S_t > h（阈值）时触发报警")
    lines.append("   - 基础控制限：")
    lines.append("       Upper_t = μ_t + k_base · σ_t")
    lines.append("       Lower_t = μ_t - k_base · σ_t")
    lines.append("       其中 k_base 为基础控制限系数（一般取 2.7~3.0）")

    # 步骤 3：深度学习趋势预测
    lines.append(f"{step}. 深度学习趋势预测")
    step += 1
    if has_dl:
        lines.append(
            "   构建深度学习时序预测模型，预测未来传感器值或健康指数（HI）："
        )
        lines.append(
            "   - 模型选型：LSTM 适合中长序列依赖建模（推荐 64~256 隐藏单元）；"
            "Transformer 适合多变量并行交互与长期依赖；"
            "AutoEncoder 适合学习正常模式，以重构误差做异常检测"
        )
        lines.append("   - 输入：过去 N 个时间步的多传感器向量 X[t-N:t]")
        lines.append("   - 输出：未来 M 步传感器预测值 X_hat[t:t+M]，"
                     "或健康指数 HI(t)")
        lines.append("   - 预测误差（异常强度）：")
        lines.append("       e_t = |X_t - X_hat_t|  或  e_t = MAE(X_t, X_hat_t)")
        if has_rul:
            lines.append("   - RUL 估计：在 HI 退化曲线上标定失效阈值，"
                         "外推至失效点的剩余时间作为 RUL(t)")
        lines.append("   - 训练损失：MSE 或加权 MSE（对近期样本加权），"
                     "可加入正则化项防止过拟合")
    else:
        lines.append(
            "   使用统计趋势分析方法预测传感器数据的未来走向："
        )
        lines.append("   - 线性回归 / Holt-Winters 指数平滑做短期趋势外推")
