    lines.append(f"{step}. 阈值自适应修正")
    step += 1
    lines.append(
        "   静态阈值的核心问题在于无法适应设备老化和工况变化，"
        "需要引入自适应修正机制："
    )
    lines.append("   - 综合报警阈值：")
    lines.append("       Threshold_t = μ_t + k_t · σ_t + α · uncertainty_t")
    lines.append("       其中：")
    lines.append("         k_t = k_base + β · health_decay(t) "
                 "- γ · recent_fpr(t)")
    lines.append("         uncertainty_t = σ_pred(t)（模型预测不确定性）")
    lines.append("         health_decay(t)：设备健康退化因子（0~1），"
                 "随运行时长单调递增")
    lines.append("         recent_fpr(t)：近期误报率反馈项，"
                 "误报偏高时自动收紧 k_t")
    lines.append("   - 参数说明：")
    lines.append("       α：模型不确定性权重（0.5~2.0，不确定性越高阈值越宽）")
    lines.append("       β：退化敏感度（控制随设备老化的阈值收紧速度）")
    lines.append("       γ：误报反馈强度（基于人工确认结果自适应调整）")
    lines.append("   - 每季度用最新运行数据重新标定 μ_t 和 σ_t 基线，"
                 "防止基线漂移")

    # 步骤 5：报警决策
    lines.append(f"{step}. 报警决策")
    step += 1
    lines.append(
        "   单一条件触发报警容易造成大量误报，需建立多条件联合决策机制："
    )
    lines.append("   - 报警触发条件（建议至少满足 ≥2 条才报警）：")
    lines.append("       (a) SPC 超限：当前值超出 Upper_t 或低于 Lower_t")
    lines.append("       (b) 预测残差异常：e_t > τ_e（残差异常阈值）")
    if has_rul:
        lines.append("       (c) RUL 下降趋势：RUL(t) 持续下降且 < RUL_critical")
    lines.append("       (d) 多传感器关联异常：≥M 个传感器同时出现异常信号")
    lines.append("   - 滞后报警机制：连续 K 个采样点满足条件才触发报警，"
                 "避免单点噪声（K 建议取 3~5）")
    lines.append("   - 报警等级划分：")
    lines.append("       提示（Info）：仅 SPC 超限，预测残差正常 → 记录观察")
    lines.append("       预警（Warning）：SPC 超限 + 预测残差异常 → 通知运维关注")
    lines.append(
        "       严重（Critical）：SPC 超限 + 预测残差异常 + RUL "
        "急剧下降 → 建议立即停机检查"
    )
    lines.append("   - 此机制可减少单点噪声导致的误报，"
                 "也能捕捉缓慢退化导致的漏报")

    # 步骤 6：落地建议
    lines.append(f"{step}. 落地建议")
    step += 1
    lines.append("   实施层面的具体建议：")
